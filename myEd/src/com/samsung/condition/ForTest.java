package com.samsung.condition;

public class ForTest {

	public static void main(String[] args) {

		System.out.println("반복 출력 해요." + 1);
		System.out.println("반복 출력 해요." + 2);
		System.out.println("반복 출력 해요." + 3);
		System.out.println("반복 출력 해요." + 4);
		System.out.println("반복 출력 해요." + 5);
		
		
		System.out.println("==========for문을 사용한 예제===========");
		
		for(int i=1; i<6; i++) {
			System.out.println("반복 출력 해요." +i);
		}

	}

}
